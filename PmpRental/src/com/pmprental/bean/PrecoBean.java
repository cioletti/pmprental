package com.pmprental.bean;

public class PrecoBean {
	private Integer parcela;
	private String preco;
	private String precoConcessao;
	public Integer getParcela() {
		return parcela;
	}
	public void setParcela(Integer parcela) {
		this.parcela = parcela;
	}
	public String getPreco() {
		return preco;
	}
	public void setPreco(String preco) {
		this.preco = preco;
	}
	public String getPrecoConcessao() {
		return precoConcessao;
	}
	public void setPrecoConcessao(String precoConcessao) {
		this.precoConcessao = precoConcessao;
	}


}
